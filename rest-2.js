// Faça uma função que obtenha seis dados de data e hora 
// (ano, mês, dia, hora, minuto, segundo) e retorne os dados de ano, mês e dia formatados (dd/MM/AAAA).

const formatDate = (year, month, day, ...otherParameters) => {
  console.log(otherParameters);
  return `${day}/${month}/${year}`;
};

// Ao chamar a função formatDate com os seguintes parâmetros:
// '1981', '7', '31', '22', '47', '12'
// O resultado esperado é: 31/7/1981
console.log(formatDate('1981', '7', '31', '22', '47', '12'));
